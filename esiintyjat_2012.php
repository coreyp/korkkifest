<p><a href="http://www.myspace.com/gasellit" target="_blank">Gasellit</a> on Pohjois-Helsinkiläinen nelihenkinen rap-yhtye. Bändi koostuu kolmesta solistista; Päkä, Hätis & Thube Hefner sekä tuottaja/dj/teknikko Musajusasta. Gasellit on perustettu 2008 ja debyyttijulkaisu, Gasellit EP, näki päivänvalon keväällä 2010. Ensimmäinen pitkäsoitto "Kiittämätön" ilmestyi 4.4.2012 (Monsp Records).</p><br>

<h2>Lady Escape</h2>

<object style="height: 390px; width: 640px"><param name="movie" value="http://www.youtube.com/v/0W2E-3W5Ad0?version=3&feature=player_detailpage"><param name="allowFullScreen" value="true"><param name="allowScriptAccess" value="always"><embed src="http://www.youtube.com/v/0W2E-3W5Ad0?version=3&feature=player_detailpage" type="application/x-shockwave-flash" allowfullscreen="true" allowScriptAccess="always" width="640" height="360"></object>

<p><a href="http://www.ladyescape.com/" target="_blank">Lady Escape</a> on helsinkiläinen indie rock -yhtye, joka on perustettu 2004. Lady Escape ammentaa musiikkiinsa vaikutteita varsin laajasti. Usein vertaukset kääntyvät yhdysvaltalaisten vaihtoehtorock-yhtyeiden puoleen. Yhtye on julkaissut kaksi albumia sekä kaksi EP-levyä. Debyyttialbumi "Unsung Land" julkaistiin 16. syyskuuta 2009. Bändin ensimmäinen albumi sai pääosin positiivisia arvioita ja toi yhtyeelle kohtalaisesti näkyvyyttä. Yhtyeen toinen albumi, "Festive Hearts" julkaistiin 4. huhtikuuta 2012. Lady Escape on soittanut suuren määrän keikkoja uransa aikana.</p><br>

