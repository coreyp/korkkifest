<h2>Lisätietoja paidasta</h2>
 
<img src="images/gildan-olive.png" width="310" height="348" alt="mallipaita 2012">

<p>Paita on Gildan ja väri Olive. Tarjolla on koot S, M, L, XL ja XXL. Lisäksi pyrimme mahdollisuuksien mukaan tilaamaan myös naisten mallia. Järjestäjät pidättävät kaikki oikeudet paitamuutoksiin.</p>

<p>Kun tilaat paidan etukäteen, varmistat ettet jää ilman omaa kokoasi ja saat paidan edullisemmin. Toki paitoja koitetaan saada myyntiin joitain kappaleita myös itse tapahtumaan. Paidan hinta paikan päällä tulee olemaan 20€. Kun tilaat paidan etukäteen, hinta on vain 15€. Paitojen myynnin tuotto käytetään edellisvuosien tapaan suoraan festarin järjestämisen kuluihin.</p>

